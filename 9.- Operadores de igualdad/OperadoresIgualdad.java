public class OperadoresIgualdad {
  public static void main(String[] args) {
    System.out.println("Iniciando");
    int a = 10,
        b = 20;

    System.out.println(a == b); //false
    System.out.println(a != b); //true

    System.out.println(a < b); //true
    System.out.println(a > b); //true

  }
}
